export function searchContent(query: string) {
  const lowerQuery = query.toLowerCase().trim();
  
  if (!lowerQuery) return [];

  // Exact title matches first
  const exactMatches = searchableContent.filter(item =>
    item.title.toLowerCase().includes(lowerQuery)
  );

  // Then keyword matches
  const keywordMatches = searchableContent.filter(item =>
    !exactMatches.includes(item) &&
    item.keywords.some(kw => kw.includes(lowerQuery))
  );

  // Then description matches
  const descriptionMatches = searchableContent.filter(item =>
    !exactMatches.includes(item) &&
    !keywordMatches.includes(item) &&
    item.description.toLowerCase().includes(lowerQuery)
  );

  return [...exactMatches, ...keywordMatches, ...descriptionMatches].slice(0, 12);
}
